/**
 * Get icon URL for a unit from the Age of Empires Fandom Wiki
 * Uses the Wikia/Fandom static image CDN
 *
 * @param {string} unitId - Unit identifier
 * @returns {string|null} Icon URL or null if no mapping exists
 */
export const getWikiIconUrl = (unitId) => {
  const iconName = unitIconMappings[unitId];
  if (!iconName) return null;

  // Fandom wiki uses a consistent pattern for AoE2 icons
  // Format: https://static.wikia.nocookie.net/ageofempires/images/hash/hash/IconName.png
  // However, the hash changes, so we'll use a different approach
  // Instead, we can construct URLs based on the wiki page structure
  return `https://ageofempires.fandom.com/wiki/Special:FilePath/${iconName}.png`;
};

